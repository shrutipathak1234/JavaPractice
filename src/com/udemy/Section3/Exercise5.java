package com.udemy.Section3;

public class Exercise5 {

    public static void main(String[] args){
        int myIntValue=5/3;
        float myFloatValue=5/3f;
        double myDoubleValue=5/3d;

        System.out.println("My Int Value = "+myIntValue);
        System.out.println("My float Value = "+myFloatValue);
        System.out.println("My double Value = "+myDoubleValue);

        double numberOfPound=200d;
        double convertedToKilograms= numberOfPound * 0.45359237d;
        System.out.println("My Converted_kilograms Value = " +convertedToKilograms);

    }
}
